def _wrap(text, indent, prefix, width):
    pad = " " * indent
    return textwrap.fill(text, width=width,
                         initial_indent=pad + prefix,
                         subsequent_indent=pad + " " * len(prefix),
                         break_long_words=False,
                         break_on_hyphens=False)


def render_article(items, width=80):
    """Render article items as wrapped markdown text.

    Item formats:
    - str:                  unnumbered paragraph at indent 0.
    - (label, text):        numbered point ("1.", "...") at indent 0.
    - (indent, prefix, txt): explicit segment with arbitrary indent.
    """
    if len(items) == 1 and isinstance(items[0], str):
        return _wrap(items[0], 0, "", width) + "\n"

    blocks = []
    for item in items:
        if isinstance(item, str):
            indent, prefix, text = 0, "", item
        elif len(item) == 2:
            label, text = item
            indent, prefix = 0, label + " "
        elif len(item) == 3:
            indent, prefix, text = item
        else:
            raise ValueError(f"Bad item shape: {item!r}")
        blocks.append(_wrap(text, indent, prefix, width))
    return "\n\n".join(blocks) + "\n"


def main():
    for chapter_subdir, articles in CHAPTERS:
        full_dir = os.path.join(PROJECT_ROOT, chapter_subdir)
        os.makedirs(full_dir, exist_ok=True)
        chapter_label = chapter_subdir.split("/")[-1]
        print(f"== {chapter_label} ==")
        for num in sorted(articles):
            content = render_article(articles[num])
            path = os.path.join(full_dir, f"Статья {num}.md")
            with open(path, "w", encoding="utf-8") as f:
                f.write(content)
            max_len = max(len(line) for line in content.splitlines())
            status = "OK" if max_len <= 80 else f"OVERFLOW ({max_len})"
            print(f"  Статья {num:>2}: {status}")


if __name__ == "__main__":
    main()
